    mainClass: "mfp-fade",
    preloader: true,
    callbacks: {
      markupParse(template) {
        setTimeout(() => {
          const iframe = template.find("iframe");
          if (iframe.length) {
            iframe.attr("allowfullscreen", "");
            iframe.attr(
              "allow",
              "encrypted-media; microphone; camera; geolocation"
            );
            iframe.attr("scrolling", "no");
